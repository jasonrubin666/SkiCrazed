function getCharData(ch: string): number[] {
  // Uppercase mapping for lowercase input
  const upper = ch.toUpperCase();
  return F[upper] || F[ch] || F['?'] || F[' '];
}

/** Measure the pixel width of a string. */
export function textWidth(text: string): number {
  return text.length * CHAR_W;
}

/** Draw text at pixel position (x, y) in the given color. */
export function drawText(
  ctx: CanvasRenderingContext2D,
  text: string,
  x: number,
  y: number,
  color: string,
): void {
  ctx.fillStyle = color;
  for (let i = 0; i < text.length; i++) {
    const data = getCharData(text[i]);
    const cx = Math.floor(x) + i * CHAR_W;
    for (let row = 0; row < CHAR_H; row++) {
      const bits = data[row];
      if (bits === 0) continue;
      for (let col = 0; col < CHAR_W; col++) {
        if ((bits >> (6 - col)) & 1) {
          ctx.fillRect(cx + col, Math.floor(y) + row, 1, 1);
        }
      }
    }
  }
}

/** Draw text centered horizontally at the given y position. */
export function drawTextCentered(
  ctx: CanvasRenderingContext2D,
  text: string,
  y: number,
  color: string,
): void {
  const w = textWidth(text);
  const x = Math.floor((GAME_WIDTH - w) / 2);
  drawText(ctx, text, x, y, color);
}

/** Draw text right-aligned ending at the given x position. */
export function drawTextRight(
  ctx: CanvasRenderingContext2D,
  text: string,
  x: number,
  y: number,
  color: string,
): void {
  const w = textWidth(text);
  drawText(ctx, text, x - w, y, color);
}

/**
 * Draw a single character at pixel position.
 * Returns the character width for chaining.
 */
export function drawChar(
  ctx: CanvasRenderingContext2D,
  ch: string,
  x: number,
  y: number,
  color: string,
): number {
  drawText(ctx, ch, x, y, color);
  return CHAR_W;
}

/**
 * Convert a text-mode row/column to pixel coordinates.
 * Apple IIe: 40 columns × 24 rows.
 */
export function gridPos(col: number, row: number): { x: number; y: number } {
  return { x: col * CHAR_W, y: row * CHAR_H };
}
